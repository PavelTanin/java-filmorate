DROP TABLE IF EXISTS friendship;
DROP TABLE IF EXISTS film_genre;
DROP TABLE IF EXISTS likes;
DROP TABLE IF EXISTS films;
DROP TABLE IF EXISTS users;
DROP TABLE IF EXISTS genres;
DROP TABLE IF EXISTS mpa_rating;

CREATE TABLE IF NOT EXISTS mpa_rating
(
    MPA_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MPA    VARCHAR
);

CREATE TABLE IF NOT EXISTS genres
(
    genre_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    genre    VARCHAR
);

CREATE TABLE IF NOT EXISTS users
(
    id        INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name      VARCHAR,
    login     VARCHAR,
    email     VARCHAR,
    birthdate DATE
);

CREATE TABLE IF NOT EXISTS films
(
    id          INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name        VARCHAR,
    description VARCHAR,
    duration    INTEGER,
    releasedate DATE,
    rate        INTEGER,
    MPA         INTEGER REFERENCES mpa_rating (MPA_id) ON DELETE RESTRICT
);

CREATE TABLE IF NOT EXISTS likes
(
    film_id INTEGER REFERENCES films (id) ON DELETE CASCADE,
    user_id INTEGER REFERENCES users (id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS film_genre
(
    film_id  INTEGER REFERENCES films (id) ON DELETE CASCADE,
    genre_id INTEGER REFERENCES genres (genre_id) ON DELETE RESTRICT
);

CREATE TABLE IF NOT EXISTS friendship
(
    user_id       INTEGER REFERENCES users (id) ON DELETE CASCADE,
    friended_user INTEGER REFERENCES users (id) ON DELETE CASCADE
);

